    private void tbl_feedbackMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_feedbackMouseClicked
        String lblQuery = "Query";
        String lblComment = "Comment";

        defaulttbl_2_view = (DefaultTableModel)tbl_feedback.getModel();
        int selectIndex = tbl_feedback.getSelectedRow();

        int id = Integer.parseInt(defaulttbl_2_view.getValueAt(selectIndex, 0).toString());

        try {
            Connection conn = DBConnection.setConnection();
            sqlQueryCheck = conn.prepareStatement("select * from tbl_interaction where msg_id=?");
            sqlQueryCheck.setInt(1, id);
            res = sqlQueryCheck.executeQuery();

            while(res.next()) {
                String fetchUnit = res.getString("unit_name");
                String fetchLec  = res.getString("lec_fname");
                String fetchDate = res.getString("date_sent");
                String fetchMsg  = res.getString("msg_content");
                String fetchlbl  = res.getString("msg_lbl");
                
                if (fetchlbl.equals(lblQuery)){
                    btnViewQuery.setVisible(true);
                    btnViewComment.setVisible(false);
                    
                    queryRetrieveUnit.setText(fetchUnit);
                    queryRetrieveLecName.setText(fetchLec);
                    queryRetrieveDateSent.setText(fetchDate);
                    queryRetrieveMsg.setText(fetchMsg);
                }  
                else if (fetchlbl.equals(lblComment)){
                    btnViewQuery.setVisible(false);
                    btnViewComment.setVisible(true);
                    
                    commentRetrieveUnit.setText(fetchUnit);
                    commentRetrieveLecName.setText(fetchLec);
                    commentRetrieveDateSent.setText(fetchDate);
                    commentRetrieveMsg.setText(fetchMsg);
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(Page4_Feedback.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tbl_feedbackMouseClicked

