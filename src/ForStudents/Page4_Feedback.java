    private void queryRetrieveLinkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_queryRetrieveLinkMouseClicked
        // downloading the document
        defaulttbl_5_download = (DefaultTableModel)tbl_feedback.getModel();
        int selectIndex = tbl_feedback.getSelectedRow();

        int id = Integer.parseInt(defaulttbl_5_download.getValueAt(selectIndex, 0).toString());
        String fileSaveAs = queryRetrieveLink.getText();
        
        try {
            Connection conn = DBConnection.setConnection();
            PreparedStatement sqlDownload = conn.prepareStatement("select * from tbl_interaction where msg_id=? ");
            sqlDownload.setInt(1, id);            
            ResultSet res = sqlDownload.executeQuery();
            
            if(res.next()) {
                Blob b = res.getBlob("document");
                
                byte[] byte_array = b.getBytes(1, (int)b.length());
                FileOutputStream fos = new FileOutputStream(fileSaveAs);
                fos.write(byte_array);
                fos.close();
                
                JOptionPane.showMessageDialog(null, "Your document will be downloaded to " + " " + fileSaveAs);
                JOptionPane.showMessageDialog(null, "File has been Downloaded successfully");
            }

            // closing the DB connection
            conn.close();
        } catch (Exception ex) {
            Logger.getLogger(Page4_Feedback.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_queryRetrieveLinkMouseClicked

