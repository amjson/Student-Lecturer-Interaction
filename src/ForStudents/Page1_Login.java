    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // operation for login inside the system
        
        // start by initializing the session 
        StudentSession startSession = new StudentSession(); 
        
        String userEmail = txtEmail.getText();     
        String userPass = String.valueOf(txtPassword.getPassword());
        
        if(userEmail.trim().equals("") || userEmail.trim().equals("someone@newmail.com")){
            JOptionPane.showMessageDialog(this, "Please Fill in your Email", "Try again", JOptionPane.ERROR_MESSAGE);
            return;
        } else if(userPass.trim().equals("") || userPass.trim().equals("yourpassword")){
            JOptionPane.showMessageDialog(this, "Please Fill in your Password", "Try again", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            try {
                // checking if user credential already exist
                Connection conn = DBConnection.setConnection();
                sqlQueryCheck = conn.prepareStatement("select * from tbl_students where email=?");
                sqlQueryCheck.setString(1, userEmail);
                res = sqlQueryCheck.executeQuery();
                
                if(!res.isBeforeFirst()) {
                    // if the credential dont match with the one in the database
                    JOptionPane.showMessageDialog(this, "Invalid Email", "Please Try again", JOptionPane.ERROR_MESSAGE);

                    // clearing the neccessary field to allow the user to key in valid Cridentials  
                    txtEmail.setText(""); 
                    txtEmail.requestFocus();
                    return;     
                } else {
                    // if the email match with the one in the database then check the password 
                    while(res.next()) {
                        // retrieve the password in the db
                        String retrievePass = res.getString("password");
                        
                        // checking if password provided matches with the one in the database

                        if (retrievePass.equals(userPass)) {
                            // using the login credentials to get the user session
                            if(startSession.isLogin(userEmail, userPass, this)){
                                // updating users status to online
                                conn = DBConnection.setConnection();
                                sqlQueryLogin = conn.prepareStatement("update tbl_students SET status='Online' WHERE email=? ");
                                sqlQueryLogin.setString(1, userEmail);

                                if (sqlQueryLogin.executeUpdate() != 0) {
                                    // redirect  to Dashboard 
                                    new Page2_Dashboard().setVisible(true);
                                    this.dispose();
                                }
                            }
                            else {
                                JOptionPane.showMessageDialog(this, "Please type correct Email or Password");
                            }
                        } else {
                            // if the credential dont match with the one in the database
                            JOptionPane.showMessageDialog(this, "Invalid Password", "Please Try again", JOptionPane.ERROR_MESSAGE);
                            
                            // clearing the neccessary field to allow the user to key in valid Cridentials  
                            txtPassword.setText(""); 
                            txtPassword.requestFocus();
                            return; 
                        }
                    }
                }
            } catch (Exception ex) {
                Logger.getLogger(Page1_Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_btnLoginActionPerformed

