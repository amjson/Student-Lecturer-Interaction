    private void feedback_table() {        
        try {
            conn = DBConnection.setConnection();
            sqlQueryCheck = conn.prepareStatement("select * from tbl_interaction");
            res = sqlQueryCheck.executeQuery();
            
            while(res.next()) {
                String feedbId     = res.getString("msg_id");
                String feedbStudId = res.getString("stud_identifier");
                String feedbFname  = res.getString("stud_fname");
                String feedbUnit   = res.getString("unit_name");
                String feedbLbl    = res.getString("msg_lbl");
                String feedbStatus = res.getString("msg_status");
                
                Object[] obj = {feedbId, feedbStudId, feedbFname, feedbUnit, feedbLbl, feedbStatus};
                
                defaulttbl_1_feedback = (DefaultTableModel)tbl_display.getModel();
                defaulttbl_1_feedback.addRow(obj);
            }
        } catch (Exception ex) {
            Logger.getLogger(Page3_Queries.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

