    private void btnSubmitFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitFileActionPerformed
        // operation for submitting a document
        // comparison variables
        String studId       = replyToRegQuery1.getText();    
        String lecId        = topBarSerialNo.getText(); 
        String unitName     = queryRetrieveUnitName.getText();
        String queryContent = replyToQueryMsg1.getText(); 
        
        // variables to be inserted in the database
        String msgStatus = "Resolved";
        String path      = docNamePath.getText();
        File docName     = new File(path);
        String date_sent = txtDate.getText();   
        String cleared   = "Yes";  
        
        try {
            InputStream inputS = new FileInputStream(docName);
            
            conn = DBConnection.setConnection();
            sqlSubmitQuery = conn.prepareStatement("update tbl_interaction set msg_status=?, link_doc=?, document=?, date_replied=?, doubt_cleared=? where stud_identifier=? and lec_identifier=? and unit_name=? and msg_content=?");
            sqlSubmitQuery.setString(1, msgStatus);
            sqlSubmitQuery.setString(2, path);
            sqlSubmitQuery.setBlob(3, inputS);
            sqlSubmitQuery.setString(4, date_sent);
            sqlSubmitQuery.setString(5, cleared);
            sqlSubmitQuery.setString(6, studId);
            sqlSubmitQuery.setString(7, lecId);
            sqlSubmitQuery.setString(8, unitName);
            sqlSubmitQuery.setString(9, queryContent);

            //if the record is inserted successfully the state of executeUpdate() will be 1
            if (sqlSubmitQuery.executeUpdate() != 0) {
                JOptionPane.showMessageDialog(null, "The query was successfully resolved.");

                // redirect 
                new Page4_Overview().setVisible(true);
                this.dispose();

                return;
            }
        } catch (Exception ex) {
            Logger.getLogger(Page3_Queries.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSubmitFileActionPerformed

    private void btnGoToReplyCommentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGoToReplyCommentActionPerformed
        jTabbedPane1.setSelectedIndex(5);
        GoBack.setVisible(true);
        
        defaulttbl_5_comment = (DefaultTableModel)tbl_display.getModel();
        int selectIndex = tbl_display.getSelectedRow();

        int id = Integer.parseInt(defaulttbl_5_comment.getValueAt(selectIndex, 0).toString());

        try {
            Connection conn = DBConnection.setConnection();
            sqlQueryCheck = conn.prepareStatement("select * from tbl_interaction where msg_id=?");
            sqlQueryCheck.setInt(1, id);
            res = sqlQueryCheck.executeQuery();

            while(res.next()) {
                String dispStudId  = res.getString("stud_identifier");
                String dispFname   = res.getString("stud_fname");
                String dispContent = res.getString("msg_content");

                replyToRegComment.setText(dispStudId);
                replyToFnameComment.setText(dispFname);
                replyToCommentMsg.setText(dispContent);
            }
        } catch (Exception ex) {
            Logger.getLogger(Page3_Queries.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGoToReplyCommentActionPerformed

