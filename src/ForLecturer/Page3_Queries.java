    private void btnReplyQueryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReplyQueryActionPerformed
        // operation for replying a query    
        // comparison variables
        String studId       = replyToRegQuery.getText();    
        String lecId        = topBarSerialNo.getText(); 
        String unitName     = queryRetrieveUnitName.getText();
        String queryContent = replyToQueryMsg.getText(); 
        
        // variables to be inserted in the database
        String msgStatus  = "Resolved";
        String msgContent = txtReplyQuery.getText();
        String date_sent  = txtDate.getText();   
        String cleared    = "Yes";            
        
        // checking if any field is empty before submitting the form
        if(msgContent.trim().equals("") || msgContent.trim().equals("start typing ...")){
            JOptionPane.showMessageDialog(null, "Cannot submit an Empty Form", "Form Submission Error!", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            try {
                conn = DBConnection.setConnection();
                sqlSubmitQuery = conn.prepareStatement("update tbl_interaction set msg_status=?, replied_msg=?, date_replied=?, doubt_cleared=? where stud_identifier=? and lec_identifier=? and unit_name=? and msg_content=?");
                sqlSubmitQuery.setString(1, msgStatus);
                sqlSubmitQuery.setString(2, msgContent);
                sqlSubmitQuery.setString(3, date_sent);
                sqlSubmitQuery.setString(4, cleared);
                sqlSubmitQuery.setString(5, studId);
                sqlSubmitQuery.setString(6, lecId);
                sqlSubmitQuery.setString(7, unitName);
                sqlSubmitQuery.setString(8, queryContent);
                
                //if the record is inserted successfully the state of executeUpdate() will be 1
                if (sqlSubmitQuery.executeUpdate() != 0) {
                    JOptionPane.showMessageDialog(null, "The query was successfully resolved.");
                            
                    // redirect 
                    new Page4_Overview().setVisible(true);
                    this.dispose();

                    return;
                }
            } catch (Exception ex) {
                Logger.getLogger(Page3_Queries.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnReplyQueryActionPerformed

    private void btnBrowseDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseDocActionPerformed
        // operation for browsing a document
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));        
        FileNameExtensionFilter extension = new FileNameExtensionFilter("Allowed File is PDF","pdf");  // file extension
        fileChooser.addChoosableFileFilter(extension);
        int load = fileChooser.showOpenDialog(null);
        
        // check if the user has selected an image
        if(load == fileChooser.APPROVE_OPTION) {
            File docName = fileChooser.getSelectedFile();
            String path  = docName.getAbsolutePath();
            docNamePath.setText(path);
        }
    }//GEN-LAST:event_btnBrowseDocActionPerformed

